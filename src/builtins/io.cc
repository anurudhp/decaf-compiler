#include <iostream>

// read_int: reads one integer from stdin
int read_int() {
	int val;
	std::cin >> val;
	return val;
}

// write_int: prints one integer to stdout
void write_int(int val) {
	std::cout << val;
}

// write_char: prints one character to stdout
void write_char(char val) {
	std::cout << val;
}